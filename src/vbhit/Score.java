package vbhit;

public class Score {
	private int miss;
	private int score;
	public Score(int miss, int score) {
		super();
		this.miss = miss;
		this.score = score;
	}
	public int getMiss() {
		return miss;
	}
	public void setMiss(int miss) {
		this.miss = miss;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	

}
